@Composable
fun ExoPlayerScreen(
    videoUri: String
){

    val context = LocalContext.current
    val exoPlayer = remember {
        ExoPlayer.Builder(context).build().apply {
            val mediaItem = MediaItem.fromUri(videoUri)
            setMediaItem(mediaItem)
            prepare()
            playWhenReady = true
        }
    }

    DisposableEffect(
        AndroidView(
            factory = {
                PlayerView(context).apply {
                    player = exoPlayer
                }
            },
            update = {
                it.player = exoPlayer
            }
        )
    ) {
        onDispose {
            exoPlayer.release()
        }
    }
}










